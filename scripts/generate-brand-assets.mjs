const root = fileURLToPath(new URL("..", import.meta.url));
const checkOnly = process.argv.includes("--check");
const generatedMarker = "<!-- generated by pnpm brand:generate; do not edit -->";
const positive = "#123f39";
const reversed = "#f7f4e8";
const wordmarkSourcePath = "src/brand/wordmark.outlines.svg";
const wordmarkSourceSha256 =
  "4f87c1fe0e200c55720896454529cfbe34eab5b647bf08f6067e8c6e642190f2";
const wordmarkFontSha256 =
  "e2291e842cf5af167122a22881a740c7f2dda7716f1e8cd76680264f4a859470";
const wordmarkFontLicenseSha256 =
  "d0283623ef57e722fd0eb688a8041589670c608ab780cd3612d06ba6f153d3fd";

function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

const wordmarkSourceBytes = await readFile(join(root, wordmarkSourcePath));
if (sha256(wordmarkSourceBytes) !== wordmarkSourceSha256) {
  throw new Error("Reviewed wordmark outline source hash drift.");
}
const wordmarkSource = wordmarkSourceBytes.toString("utf8");
const wordmarkMatch = wordmarkSource.match(
  /(<defs>[\s\S]*<\/defs>)\n<g fill="rgb\(0%, 0%, 0%\)" fill-opacity="1">([\s\S]*?)<\/g>\n<\/svg>\n?$/,
);
if (!wordmarkMatch) {
  throw new Error("Reviewed wordmark outline source has an unsupported structure.");
}
const wordmarkDefinitions = wordmarkMatch[1];
const wordmarkUses = wordmarkMatch[2].trim();

function svgMark(color) {
  const rectangles = brandGeometry.rectangles
    .map(({ x, y, width, height }) => `<rect x="${x}" y="${y}" width="${width}" height="${height}"/>`)
    .join("");
  return `${generatedMarker}\n<svg xmlns="http://www.w3.org/2000/svg" viewBox="${brandGeometry.viewBox}" fill="${color}">${rectangles}</svg>\n`;
}

function svgLockup(color, identity, compact = false) {
  const rectangles = brandGeometry.rectangles
    .map(({ x, y, width, height }) => `<rect x="${x}" y="${y}" width="${width}" height="${height}"/>`)
    .join("");
  const width = compact ? 216 : 260;
  const transform = compact ? "translate(58 10) scale(0.8)" : "translate(58 7)";
  return `${generatedMarker}\n<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" data-brand-identity="${identity}" viewBox="0 0 ${width} 48" fill="${color}">${wordmarkDefinitions}<g>${rectangles}</g><g transform="${transform}">${wordmarkUses}</g></svg>\n`;
}

function crc32(bytes) {
  let crc = 0xffffffff;
  for (const byte of bytes) {
    crc ^= byte;
    for (let bit = 0; bit < 8; bit += 1) {
      crc = (crc >>> 1) ^ (crc & 1 ? 0xedb88320 : 0);
    }
  }
  return (crc ^ 0xffffffff) >>> 0;
}

function pngChunk(type, data) {
  const typeBytes = Buffer.from(type);
  const length = Buffer.alloc(4);
  length.writeUInt32BE(data.length);
  const checksum = Buffer.alloc(4);
  checksum.writeUInt32BE(crc32(Buffer.concat([typeBytes, data])));
  return Buffer.concat([length, typeBytes, data, checksum]);
}

function insideRoundedSquare(x, y, size) {
  const radius = size * 0.18;
  const insetX = Math.max(radius - x, 0, x - (size - radius - 1));
  const insetY = Math.max(radius - y, 0, y - (size - radius - 1));
  return insetX * insetX + insetY * insetY <= radius * radius;
}

function insideMark(x, y, size) {
  const sourceX = ((x + 0.5) / size) * 48;
  const sourceY = ((y + 0.5) / size) * 48;
  return brandGeometry.rectangles.some(
    (rectangle) =>
      sourceX >= rectangle.x &&
      sourceX < rectangle.x + rectangle.width &&
      sourceY >= rectangle.y &&
      sourceY < rectangle.y + rectangle.height,
  );
}

function faviconPng(size) {
  const scanlines = Buffer.alloc((size * 4 + 1) * size);
  const background = [18, 63, 57, 255];
  const foreground = [247, 244, 232, 255];
  for (let y = 0; y < size; y += 1) {
    const row = y * (size * 4 + 1);
    scanlines[row] = 0;
    for (let x = 0; x < size; x += 1) {
      const offset = row + 1 + x * 4;
      const pixel = insideMark(x, y, size) ? foreground : background;
      const alpha = insideRoundedSquare(x, y, size) ? pixel[3] : 0;
      scanlines.set([pixel[0], pixel[1], pixel[2], alpha], offset);
    }
  }
  const header = Buffer.alloc(13);
  header.writeUInt32BE(size, 0);
  header.writeUInt32BE(size, 4);
  header[8] = 8;
  header[9] = 6;
  return Buffer.concat([
    Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]),
    pngChunk("IHDR", header),
    pngChunk("IDAT", deflateSync(scanlines, { level: 9 })),
    pngChunk("IEND", Buffer.alloc(0)),
  ]);
}

function faviconIco(pngs) {
  const header = Buffer.alloc(6 + pngs.length * 16);
  header.writeUInt16LE(0, 0);
  header.writeUInt16LE(1, 2);
  header.writeUInt16LE(pngs.length, 4);
  let offset = header.length;
  pngs.forEach(({ size, bytes }, index) => {
    const entry = 6 + index * 16;
    header[entry] = size;
    header[entry + 1] = size;
    header[entry + 2] = 0;
    header[entry + 3] = 0;
    header.writeUInt16LE(1, entry + 4);
    header.writeUInt16LE(32, entry + 6);
    header.writeUInt32LE(bytes.length, entry + 8);
    header.writeUInt32LE(offset, entry + 12);
    offset += bytes.length;
  });
  return Buffer.concat([header, ...pngs.map(({ bytes }) => bytes)]);
}

const assets = new Map();
for (const [id, source] of [
  ["mark-positive", svgMark(positive)],
  ["mark-reversed", svgMark(reversed)],
  ["product-lockup-positive", svgLockup(positive, "product-lockup")],
  ["product-lockup-reversed", svgLockup(reversed, "product-lockup")],
  ["compact-role-lockup-positive", svgLockup(positive, "compact-role-lockup", true)],
  ["compact-role-lockup-reversed", svgLockup(reversed, "compact-role-lockup", true)],
  ["merchant-fallback-positive", svgLockup(positive, "merchant-fallback", true)],
  ["merchant-fallback-reversed", svgLockup(reversed, "merchant-fallback", true)],
]) {
  assets.set(`public/brand/${id}.svg`, Buffer.from(source));
}
const faviconFrames = [16, 32, 48].map((size) => ({ size, bytes: faviconPng(size) }));
for (const frame of faviconFrames) {
  assets.set(`public/brand/favicon-${frame.size}.png`, frame.bytes);
}
assets.set("src/app/favicon.ico", faviconIco(faviconFrames));

function dimensions(path) {
  if (path.endsWith(".ico")) return { frames: [16, 32, 48] };
  const png = path.match(/favicon-(\d+)\.png$/);
  if (png) return { width: Number(png[1]), height: Number(png[1]) };
  if (path.includes("mark-")) return { viewBox: brandGeometry.viewBox };
  return { viewBox: path.includes("compact") || path.includes("merchant") ? "0 0 216 48" : "0 0 260 48" };
}

function staticIdentityMetadata(outputPath) {
  if (!outputPath.endsWith(".svg")) return {};
  const staticVariant = outputPath.includes("-reversed.") ? "reversed" : "positive";
  if (outputPath.includes("/mark-")) {
    return { identityId: "mark-only", role: "mark-only", staticVariant };
  }
  for (const identityId of [
    "product-lockup",
    "compact-role-lockup",
    "merchant-fallback",
  ]) {
    if (outputPath.includes(`/${identityId}-`)) {
      return { identityId, role: identityId, staticVariant };
    }
  }
  return {};
}

