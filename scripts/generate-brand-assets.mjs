function validateGeneratedContract() {
  const unsafeSvg = /<(?:script|image|foreignObject)\b|\bon\w+\s*=|\burl\s*\(/i;
  for (const [path, bytes] of assets) {
    if (!path.endsWith(".svg")) continue;
    const source = bytes.toString("utf8");
    if (!source.startsWith(generatedMarker) || unsafeSvg.test(source)) {
      throw new Error(`Unsafe or unmarked generated SVG: ${path}`);
    }
    if (!source.includes(`viewBox="${dimensions(path).viewBox}"`)) {
      throw new Error(`Generated SVG dimension drift: ${path}`);
    }
    if (/<text\b|font-family|@font-face/i.test(source)) {
      throw new Error(`Generated SVG contains a live font dependency: ${path}`);
    }
    const references = [
      ...source.matchAll(/\b(?:href|xlink:href)\s*=\s*"([^"]+)"/gi),
    ].map((match) => match[1]);
    if (references.some((reference) => !/^#glyph-0-\d+$/.test(reference))) {
      throw new Error(`Generated SVG contains a non-local reference: ${path}`);
    }
  }
  if (
    brandGeometry.rectangles.some(
      ({ x, y, width, height }) =>
        ![x, y, width, height].every(Number.isFinite) ||
        x < 0 ||
        y < 0 ||
        width <= 0 ||
        height <= 0 ||
        x + width > 48 ||
        y + height > 48,
    )
  ) {
    throw new Error("Canonical mark geometry must stay inside its 48-unit viewBox.");
  }
  const icon = assets.get("src/app/favicon.ico");
  if (
    !icon ||
    icon.readUInt16LE(0) !== 0 ||
    icon.readUInt16LE(2) !== 1 ||
    icon.readUInt16LE(4) !== 3 ||
    [0, 1, 2].some((index) => icon[6 + index * 16] !== [16, 32, 48][index])
  ) {
    throw new Error("Favicon ICO must contain exact 16/32/48 PNG frames.");
  }
  validateIdentityContract({ sourceIdentityIds: brandIdentityIds, manifest });
}

validateGeneratedContract();

async function assertBytes(path, expected) {
  const absolute = join(root, path);
  let actual;
  try {
    actual = await readFile(absolute);
  } catch {
    throw new Error(`Missing generated brand asset: ${path}`);
  }
  if (!actual.equals(expected)) throw new Error(`Generated brand asset drift: ${path}`);
}

if (checkOnly) {
  for (const [path, bytes] of assets) await assertBytes(path, bytes);
  await assertBytes("src/brand/assets.manifest.json", manifestBytes);
  const declaredPublic = new Set([...assets.keys()].filter((path) => path.startsWith("public/brand/")));
  const publicFiles = await readdir(join(root, "public/brand"));
  for (const file of publicFiles) {
    const path = `public/brand/${file}`;
    if (!declaredPublic.has(path)) throw new Error(`Undeclared brand output: ${path}`);
  }
  if (new Set(manifest.assets.map(({ sha256: hash }) => hash)).size !== manifest.assets.length) {
    throw new Error("Duplicate brand output bytes are not permitted.");
  }
  console.log(`Verified ${manifest.assets.length} deterministic brand assets.`);
} else {
  for (const [path, bytes] of assets) {
    await mkdir(dirname(join(root, path)), { recursive: true });
    await writeFile(join(root, path), bytes);
  }
  await mkdir(join(root, "src/brand"), { recursive: true });
  await writeFile(join(root, "src/brand/assets.manifest.json"), manifestBytes);
  console.log(`Generated ${manifest.assets.length} brand assets from ${relative(root, import.meta.filename)}.`);
}
