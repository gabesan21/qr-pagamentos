import { brandGeometry, brandIdentityIds } from "../src/brand/geometry.ts";

const root = fileURLToPath(new URL("..", import.meta.url));
const checkOnly = process.argv.includes("--check");
const generatedMarker = "<!-- generated by pnpm brand:generate; do not edit -->";
const positive = "#123f39";
const reversed = "#f7f4e8";

function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

function svgMark(color) {
  const rectangles = brandGeometry.rectangles
    .map(({ x, y, width, height }) => `<rect x="${x}" y="${y}" width="${width}" height="${height}"/>`)
    .join("");
  return `${generatedMarker}\n<svg xmlns="http://www.w3.org/2000/svg" viewBox="${brandGeometry.viewBox}" fill="${color}">${rectangles}</svg>\n`;
}

function svgLockup(color, identity, compact = false) {
  const rectangles = brandGeometry.rectangles
    .map(({ x, y, width, height }) => `<rect x="${x}" y="${y}" width="${width}" height="${height}"/>`)
    .join("");
  const width = compact ? 180 : 244;
  const fontSize = compact ? 21 : 26;
  const textY = compact ? 32 : 34;
  return `${generatedMarker}\n<svg xmlns="http://www.w3.org/2000/svg" data-brand-identity="${identity}" viewBox="0 0 ${width} 48" fill="${color}"><g>${rectangles}</g><text x="58" y="${textY}" font-family="'IBM Plex Sans',sans-serif" font-size="${fontSize}" font-weight="600">QR Pagamentos</text></svg>\n`;
}

function crc32(bytes) {
  let crc = 0xffffffff;
  for (const byte of bytes) {
    crc ^= byte;
    for (let bit = 0; bit < 8; bit += 1) {
      crc = (crc >>> 1) ^ (crc & 1 ? 0xedb88320 : 0);
    }
  }
  return (crc ^ 0xffffffff) >>> 0;
}

function pngChunk(type, data) {
  const typeBytes = Buffer.from(type);
  const length = Buffer.alloc(4);
  length.writeUInt32BE(data.length);
  const checksum = Buffer.alloc(4);
  checksum.writeUInt32BE(crc32(Buffer.concat([typeBytes, data])));
  return Buffer.concat([length, typeBytes, data, checksum]);
}

function insideRoundedSquare(x, y, size) {
  const radius = size * 0.18;
  const insetX = Math.max(radius - x, 0, x - (size - radius - 1));
  const insetY = Math.max(radius - y, 0, y - (size - radius - 1));
  return insetX * insetX + insetY * insetY <= radius * radius;
}

function insideMark(x, y, size) {
  const sourceX = ((x + 0.5) / size) * 48;
  const sourceY = ((y + 0.5) / size) * 48;
  return brandGeometry.rectangles.some(
    (rectangle) =>
      sourceX >= rectangle.x &&
      sourceX < rectangle.x + rectangle.width &&
      sourceY >= rectangle.y &&
      sourceY < rectangle.y + rectangle.height,
  );
}

function faviconPng(size) {
  const scanlines = Buffer.alloc((size * 4 + 1) * size);
  const background = [18, 63, 57, 255];
  const foreground = [247, 244, 232, 255];
  for (let y = 0; y < size; y += 1) {
    const row = y * (size * 4 + 1);
    scanlines[row] = 0;
    for (let x = 0; x < size; x += 1) {
      const offset = row + 1 + x * 4;
      const pixel = insideMark(x, y, size) ? foreground : background;
      const alpha = insideRoundedSquare(x, y, size) ? pixel[3] : 0;
      scanlines.set([pixel[0], pixel[1], pixel[2], alpha], offset);
    }
  }
  const header = Buffer.alloc(13);
  header.writeUInt32BE(size, 0);
  header.writeUInt32BE(size, 4);
  header[8] = 8;
  header[9] = 6;
  return Buffer.concat([
    Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]),
    pngChunk("IHDR", header),
    pngChunk("IDAT", deflateSync(scanlines, { level: 9 })),
    pngChunk("IEND", Buffer.alloc(0)),
  ]);
}

function faviconIco(pngs) {
  const header = Buffer.alloc(6 + pngs.length * 16);
  header.writeUInt16LE(0, 0);
  header.writeUInt16LE(1, 2);
  header.writeUInt16LE(pngs.length, 4);
  let offset = header.length;
  pngs.forEach(({ size, bytes }, index) => {
    const entry = 6 + index * 16;
    header[entry] = size;
    header[entry + 1] = size;
    header[entry + 2] = 0;
    header[entry + 3] = 0;
    header.writeUInt16LE(1, entry + 4);
    header.writeUInt16LE(32, entry + 6);
    header.writeUInt32LE(bytes.length, entry + 8);
    header.writeUInt32LE(offset, entry + 12);
    offset += bytes.length;
  });
  return Buffer.concat([header, ...pngs.map(({ bytes }) => bytes)]);
}

const assets = new Map();
for (const [id, source] of [
  ["mark-positive", svgMark(positive)],
  ["mark-reversed", svgMark(reversed)],
  ["product-lockup-positive", svgLockup(positive, "product-lockup")],
  ["product-lockup-reversed", svgLockup(reversed, "product-lockup")],
  ["compact-role-lockup-positive", svgLockup(positive, "compact-role-lockup", true)],
  ["compact-role-lockup-reversed", svgLockup(reversed, "compact-role-lockup", true)],
  ["merchant-fallback-positive", svgLockup(positive, "merchant-fallback", true)],
  ["merchant-fallback-reversed", svgLockup(reversed, "merchant-fallback", true)],
]) {
  assets.set(`public/brand/${id}.svg`, Buffer.from(source));
}
const faviconFrames = [16, 32, 48].map((size) => ({ size, bytes: faviconPng(size) }));
for (const frame of faviconFrames) {
  assets.set(`public/brand/favicon-${frame.size}.png`, frame.bytes);
}
assets.set("src/app/favicon.ico", faviconIco(faviconFrames));

function dimensions(path) {
  if (path.endsWith(".ico")) return { frames: [16, 32, 48] };
  const png = path.match(/favicon-(\d+)\.png$/);
  if (png) return { width: Number(png[1]), height: Number(png[1]) };
  if (path.includes("mark-")) return { viewBox: brandGeometry.viewBox };
  return { viewBox: path.includes("compact") || path.includes("merchant") ? "0 0 180 48" : "0 0 244 48" };
}

const manifest = {
  version: 1,
  family: "QR Pagamentos settlement mark",
  identities: brandIdentityIds,
  provenance: {
    author: "QR Pagamentos project",
    license: "Project-owned original artwork",
    source: "src/brand/geometry.ts",
    statement:
      "Original repository-native geometry. No stock, trademark-derived, remote, or generative artwork is included.",
  },
  usage: {
    clearSpace: "one-sixth of the rendered mark width",
    minimumSizes: { mark: 16, productLockup: 120, compactRoleLockup: 112, merchantFallback: 112 },
    inlineColor: "currentColor from a semantic foreground token",
    accessibleNaming:
      "Hide a mark beside visible identity text; give a standalone meaningful mark one caller-supplied localized accessible name.",
  },
  assets: [...assets].map(([outputPath, bytes]) => ({
    id: outputPath.replace(/^.*\//, "").replace(/\.[^.]+$/, ""),
    role: outputPath.includes("favicon") ? "browser-icon" : outputPath.match(/([^/]+)\.(?:svg)$/)?.[1],
    canonicalSource: "src/brand/geometry.ts",
    outputPath,
    mime: outputPath.endsWith(".svg")
      ? "image/svg+xml"
      : outputPath.endsWith(".png")
        ? "image/png"
        : "image/x-icon",
    intrinsic: dimensions(outputPath),
    colorMode: outputPath.includes("reversed") ? "reversed-static" : outputPath.includes("favicon") ? "favicon-fixed" : "positive-static",
    accessibilityMode: outputPath.includes("favicon") ? "decorative-browser-chrome" : "consumer-supplied",
    generated: true,
    sha256: sha256(bytes),
  })),
};
const manifestBytes = Buffer.from(`${JSON.stringify(manifest, null, 2)}\n`);

function validateGeneratedContract() {
  const unsafeSvg = /<(?:script|image|foreignObject)\b|\bon\w+\s*=|\b(?:href|xlink:href)\s*=/i;
  for (const [path, bytes] of assets) {
    if (!path.endsWith(".svg")) continue;
    const source = bytes.toString("utf8");
    if (!source.startsWith(generatedMarker) || unsafeSvg.test(source)) {
      throw new Error(`Unsafe or unmarked generated SVG: ${path}`);
    }
    if (!source.includes(`viewBox="${dimensions(path).viewBox}"`)) {
      throw new Error(`Generated SVG dimension drift: ${path}`);
    }
  }
  if (
    brandGeometry.rectangles.some(
      ({ x, y, width, height }) =>
        ![x, y, width, height].every(Number.isFinite) ||
        x < 0 ||
        y < 0 ||
        width <= 0 ||
        height <= 0 ||
        x + width > 48 ||
        y + height > 48,
    )
  ) {
    throw new Error("Canonical mark geometry must stay inside its 48-unit viewBox.");
  }
  const icon = assets.get("src/app/favicon.ico");
  if (
    !icon ||
    icon.readUInt16LE(0) !== 0 ||
    icon.readUInt16LE(2) !== 1 ||
    icon.readUInt16LE(4) !== 3 ||
    [0, 1, 2].some((index) => icon[6 + index * 16] !== [16, 32, 48][index])
  ) {
    throw new Error("Favicon ICO must contain exact 16/32/48 PNG frames.");
  }
}

validateGeneratedContract();

async function assertBytes(path, expected) {
  const absolute = join(root, path);
  let actual;
  try {
    actual = await readFile(absolute);
  } catch {
    throw new Error(`Missing generated brand asset: ${path}`);
  }
  if (!actual.equals(expected)) throw new Error(`Generated brand asset drift: ${path}`);
}

if (checkOnly) {
  for (const [path, bytes] of assets) await assertBytes(path, bytes);
  await assertBytes("src/brand/assets.manifest.json", manifestBytes);
  const declaredPublic = new Set([...assets.keys()].filter((path) => path.startsWith("public/brand/")));
  const publicFiles = await readdir(join(root, "public/brand"));
  for (const file of publicFiles) {
    const path = `public/brand/${file}`;
    if (!declaredPublic.has(path)) throw new Error(`Undeclared brand output: ${path}`);
  }
  if (new Set(manifest.assets.map(({ sha256: hash }) => hash)).size !== manifest.assets.length) {
    throw new Error("Duplicate brand output bytes are not permitted.");
  }
  console.log(`Verified ${manifest.assets.length} deterministic brand assets.`);
} else {
  for (const [path, bytes] of assets) {
    await mkdir(dirname(join(root, path)), { recursive: true });
    await writeFile(join(root, path), bytes);
  }
  await mkdir(join(root, "src/brand"), { recursive: true });
  await writeFile(join(root, "src/brand/assets.manifest.json"), manifestBytes);
  console.log(`Generated ${manifest.assets.length} brand assets from ${relative(root, import.meta.filename)}.`);
}
